# Sistema que realiza a soma entre dois números 
# Faremos as variáveis, o int é para números inteiros, input é para entradas de dados 
n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))
# Agora,só realizar a soma 
soma = n1 + n2
# O print é para exibir os dados, a saída deles
print("o resultado é:")
print(soma)
